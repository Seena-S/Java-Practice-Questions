
public class Operations {
	double c;
	void addition(double a,double b){
		c=a+b;
	}
	void subtraction(double a,double b){
		c=a-b;
	}
	void multiplication(double a,double b){
		c=a*b;
	}
	void division(double a,double b){
		c=a/b;
	}
	void displayResult() {
		System.out.println("Result is: "+c);
	}

}
